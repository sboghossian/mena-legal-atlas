#!/usr/bin/env python3
"""Benchmark: Legal Data Hunter (live status.json) vs MENA Legal Atlas, MENA slice."""
import json, urllib.parse, pathlib, datetime

HERE = pathlib.Path(__file__).resolve().parent
LDH = json.load(open(HERE / "ldh-status.json"))
ATLAS = json.load(open(HERE.parent / "phase-1-atlas" / "mena-legal-atlas.json"))

MENA = ["AE","BH","KW","OM","QA","SA","EG","IQ","JO","LB","PS","SY","DZ","LY",
        "MA","MR","TN","YE","SD","SO","DJ","KM","IR","IL","TR"]

def dom(u):
    try: return urllib.parse.urlparse(u).netloc.replace("www.","").lower()
    except: return ""

# --- LDH MENA aggregates ---
bc = LDH["by_country"]
ldh_total = ldh_complete = ldh_blocked = ldh_docs = 0
ldh_dom = set(); ldh_per = {}
for cc in MENA:
    c = bc.get(cc, {})
    ldh_total += c.get("total",0); ldh_complete += c.get("complete",0); ldh_blocked += c.get("blocked",0)
    ldh_per[cc] = (c.get("total",0), c.get("complete",0))
for s in LDH["sources"]:
    if s.get("country") in MENA:
        d = dom(s.get("url",""))
        if d: ldh_dom.add(d)
        ldh_docs += s.get("total_records",0) or 0

# --- Atlas MENA aggregates ---
atlas_addl = 0; atlas_dom = set(); cov_complete = 0
cats = lambda e: sum([bool((e.get('authorities',{}) or {}).get('official_gazette')),
    bool((e.get('authorities',{}) or {}).get('consolidated')),
    bool((e.get('authorities',{}) or {}).get('apex_courts')),
    bool((e.get('authorities',{}) or {}).get('key_regulators')) or bool(e.get('additional_sources')),
    any(x.get('law_key')=='constitution' for x in (e.get('key_legislation') or []))])
for cc in MENA:
    e = ATLAS.get(cc, {})
    for a in (e.get("additional_sources",[]) or []):
        atlas_addl += 1
        if a.get("domain"): atlas_dom.add(a["domain"].lower())
    if cats(e) >= 5: cov_complete += 1

new_vs_ldh = sorted(d for d in atlas_dom if d and not any(d==x or d.endswith("."+x) or x.endswith("."+d) for x in ldh_dom))

md = f"""# Benchmark: Legal Data Hunter vs MENA Legal Atlas — MENA coverage

> Data: Legal Data Hunter live `status.json` (generated {LDH['generated_at'][:10]}) +
> this atlas's compiled `mena-legal-atlas.json`. MENA = {len(MENA)} jurisdictions.

## Headline

| | Legal Data Hunter | MENA Legal Atlas (this repo) |
|---|---|---|
| Scope | Global ({len(bc)} countries) | MENA-only ({len(MENA)} jurisdictions) |
| Sources, global | {LDH['summary']['total']:,} ({LDH['summary']['percent_complete']}% complete) | — |
| Sources, MENA | {ldh_total} ({round(100*ldh_complete/max(ldh_total,1))}% complete) | +{atlas_addl} official sources mapped |
| MENA sources producing data | {ldh_complete} working collection scripts | 0 (maps sources, does not retrieve) |
| MENA source coverage | {round(100*ldh_complete/max(ldh_total,1))}% of its own sources working | {round(100*cov_complete/len(MENA))}% of jurisdictions complete across 5 categories |
| Constitutions located | (per source) | 21 / 25 (via Constitute Project / WIPO Lex) |

## The complementary read (honest)

- **Legal Data Hunter wins on retrieval.** It runs {ldh_complete} working MENA collection
  scripts that actually produce data. We map sources; we do not retrieve documents.
- **Legal Data Hunter's MENA slice is its weak slice.** Globally {LDH['summary']['percent_complete']}% of its sources are
  complete; across MENA only {round(100*ldh_complete/max(ldh_total,1))}% ({ldh_complete}/{ldh_total}) are. The region is blocked, not missing.
- **The atlas is the MENA expansion map for LDH.** We surfaced **{len(new_vs_ldh)} official
  source domains** not currently tracked in LDH's MENA set — direct candidates for new
  collection scripts upstream.

## Per-country: LDH sources (complete/total) vs atlas sources mapped

| CC | Country | LDH complete/total | Atlas sources mapped |
|----|---------|--------------------|----------------------|
"""
for cc in MENA:
    t,c = ldh_per.get(cc,(0,0))
    name = ATLAS.get(cc,{}).get("country", bc.get(cc,{}).get("name",cc))
    addl = len(ATLAS.get(cc,{}).get("additional_sources",[]) or [])
    md += f"| {cc} | {name} | {c}/{t} | +{addl} |\n"

md += f"""
## Sources we surfaced that Legal Data Hunter doesn't track ({len(new_vs_ldh)})

{', '.join(new_vs_ldh[:60])}{' …' if len(new_vs_ldh)>60 else ''}

_Generated by `viz/benchmark_ldh.py`. LDH data is public (status.json); no API key required._
"""

out = HERE.parent / "phase-2-enrichment" / "BENCHMARK-vs-legal-data-hunter.md"
out.write_text(md)
print(f"LDH global: {LDH['summary']['total']} sources, {LDH['summary']['percent_complete']}% complete")
print(f"LDH MENA:   {ldh_total} sources, {ldh_complete} complete ({round(100*ldh_complete/max(ldh_total,1))}%), ~{ldh_docs:,} docs")
print(f"Atlas MENA: +{atlas_addl} sources, {cov_complete}/25 complete ({round(100*cov_complete/25)}%)")
print(f"Sources we have that LDH lacks: {len(new_vs_ldh)}")
print(f"wrote {out}")
